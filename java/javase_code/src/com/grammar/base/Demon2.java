package com.grammar.base;

public class Demon2 {
    public static void main(String[] args) {
        //八大基本数据类型

        //整数型
        byte num1=10;
        int num2=20;    //最常用
        short num3=30;
        long num4=30L;  //Long类型要在数字后面加个L   不加符号为默认数据类型

        //浮点型
        float num5=50.1F;   //float类型要在数字后面加个F
        double num6=3.141592653589793238462643;

        //字符型
        char name='A';

        //字符串，String不是关键字，而是类
        String name1="张三";

        //boolean型
        boolean flag=true;
        //boolean flag=false;

    }
}

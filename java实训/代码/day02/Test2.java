package day02;

public class Test2 {
	public static void main(String[] args) {
		//格式：  数据类型  标识符=值
		//数据类型 ---String 字符串类型  使用""赋值
		//注意：  java中的""和''是不同
		String name1="zhang";
		System.out.println(name1);
		String name2;//定义数据类型
		name2="li"; // 给数据类型初始化值
		//注意：没有初始化的变量不能使用
		System.out.println(name2);
		name2="lisi";//一个变量的值只有一个，并且是最后一个
		System.out.println(name2);
	}

}
